#include "3-calc.h"

/**
 * op_add - performs addition of two numbers
 * @a: the first number
 * @b: the second number
 * Return: the total
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - performs subtraction of two numbers
 * @a: the first number
 * @b: the second number
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - performs multiplication of two numbers
 * @a: the first number
 * @b: the second number
 * Return: the product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - performs division of two numbers
 * @a: the first number
 * @b: the second number
 * Return: the quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the remainder of a division
 * @a: the first number
 * @b: the second number
 * Return: the remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
